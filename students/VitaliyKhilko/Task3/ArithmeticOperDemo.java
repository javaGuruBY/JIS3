public class ArithmeticOperDemo {
    public static void main(String[] args){
        int a = 75;
        int b = 31;
        int sum = a + b;
        System.out.println("a + b = " + a + " + " + b + " = " + sum);

        int c = 40;
        int d = 20;
        int dif = c - d;
        System.out.println("c - d = " + c + " - " + d + " = " + dif);

        int e = 10;
        int f = 35;
        int multi = e * f;
        System.out.println("e * f = " + e + " * " + f + " = " + multi);

        int g = 900;
        int h = 300;
        float div = (float)g / h;
        System.out.println("g / h = " + g + " / " + h + " = " + div);

    }
}
